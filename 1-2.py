#数値型の変数

num=5
print(num)

bin_val=0b0101  #2進数
print(bin_val)

oct_val=0x165   #8進数
print(oct_val)

hex_val=0xfaa   #16進数
print(hex_val)

#float型
x=0.0012
print(x)

x=1.2e-3
print(x)

#複素数定義
c1=2+3j
print(c1)
c2=complex(5,6)
print(c2)

#実部と虚部を出力
print(c1.real)
print(c2.imag)